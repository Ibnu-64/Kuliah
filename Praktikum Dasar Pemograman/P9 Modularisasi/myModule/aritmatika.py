def tambah(a,b):
    """fungsi untuk menambahkan dua bilangan"""
    return a+b


def kurang(a,b):
    """fungsi untuk mengurangkan dua bilangan"""
    return a-b

def kali(a,b):
    """fungsi untuk mengalikan dua bilangan"""
    return a*b

def bagi(a,b):
    """fungsi untuk membagi dua bilangan"""
    return a/b